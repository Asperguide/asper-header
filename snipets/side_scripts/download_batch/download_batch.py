import re
import os
from time import sleep
from typing import List, Dict
from urllib.parse import unquote, urlparse

import requests


def handle_special_cases(base_domain: str) -> str:
    # Handle known subdomains
    if base_domain.endswith("imgur.com"):
        return "imgur.com"
    return base_domain  # fallback


def get_dynamic_headers(url: str) -> Dict[str, str]:
    domain = urlparse(url)
    base_domain: str = handle_special_cases(domain.netloc)
    referer = f"{domain.scheme}://{base_domain}/"
    return {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/115.0.0.0 Safari/537.36",
        "Accept": "image/avif,image/webp,image/apng,image/*,*/*;q=0.8",
        "Referer": referer,
        "Connection": "keep-alive",
    }


def clean_filename(url: str) -> str:
    file_name_inner = url.split("/")[-1]
    file_name_inner = unquote(file_name_inner)
    file_name_inner = re.sub(r"[^A-Za-z0-9.\-]", "_", file_name_inner)
    return file_name_inner


def download_set(url_set: List[str], dest_dir: str) -> None:
    for url in url_set:
        try:
            # Extract the file name from the URL
            file_name = clean_filename(url.split("/")[-1])
            file_path = os.path.join(dest_dir, file_name)

            print(f"⬇️ Downloading {file_name} from {url} ...")

            # Send request
            response = requests.get(url, timeout=10)
            if response.status_code != 200:
                print(
                    f"⚠️ normal query failed (code: {response.status_code}), attempting to query while mimicking a browser"
                )
                response = requests.get(
                    url,
                    timeout=10,
                    headers=get_dynamic_headers(url)
                )
            response.raise_for_status()  # Raise error if download failed

            # Save file
            with open(file_path, "wb") as f:
                f.write(response.content)

            print(f"✅ Saved to {file_path}")

        except requests.exceptions.RequestException as e:
            print(f"❌ Failed to download {url}: {e}")


